    private void updateBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBActionPerformed

        String sqlstmt="update studentdata Set Name=?,Registeration_No=?,Department=?,Semester=?,Section=?,Address=?,Email=? where Registeration_No='"+Integer.parseInt(regnosearchF.getText())+"'";

        try {

            pst=con.prepareStatement(sqlstmt);
            pst.setString(1, nameF.getText());
            pst.setInt(2, Integer.parseInt(regnoF.getText()));
            pst.setString(3, depttF.getText());
            pst.setString(4, semesterF.getText());
            pst.setString(5, sectionF.getText());
            pst.setString(6, addressF.getText());
            pst.setString(7, emailF.getText());

            pst.execute();
            JOptionPane.showMessageDialog(null, "Data updated successfully!");

            nameF.setText("");
            regnoF.setText("");
            depttF.setText("");
            semesterF.setText("");
            sectionF.setText("");
            addressF.setText("");
            emailF.setText("");
            regnosearchF.setText("");

        } catch (SQLException ex) {
            
        }
    }//GEN-LAST:event_updateBActionPerformed

